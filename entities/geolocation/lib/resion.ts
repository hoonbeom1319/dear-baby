export type ReverseGeoLike = {
    principalSubdivision?: string;
    city?: string;
    locality?: string;
};

export type RegionCode = keyof typeof REGIONS;

export type MatchedRegion = {
    code: RegionCode;
    displayName: string;
    subDisplayName?: string;
    label: string;
};

function normalizeRegionName(name?: string): string {
    if (!name) {
        return '';
    }

    return name
        .trim()
        .replace(/\([^)]*\)/g, '')
        .replace(/\[[^\]]*\]/g, '')
        .replace(/^[^0-9A-Za-z가-힣]+/, '')
        .replace(/[^0-9A-Za-z가-힣]+$/, '')
        .replace(/[^0-9A-Za-z가-힣\s]/g, '')
        .replace(/\s+/g, '')
        .replace('특별자치도', '도')
        .replace('자치도', '도')
        .replace('특별자치시', '시')
        .replace('특별시', '시')
        .replace('광역시', '시')
        .replace('자치시', '시');
}

function findRegionCodeByLevel1(level1?: string): RegionCode | undefined {
    const normalizedLevel1 = normalizeRegionName(level1);
    if (!normalizedLevel1) {
        return undefined;
    }

    const exactMatchedCode = Object.entries(REGIONS).find(
        ([, region]) => normalizeRegionName(region.displayName) === normalizedLevel1
    )?.[0];
    if (exactMatchedCode) {
        return exactMatchedCode as RegionCode;
    }

    const normalizedLevel1Base = normalizedLevel1.replace(/[시도]$/, '');
    const fuzzyMatchedCode = Object.entries(REGIONS).find(([, region]) => {
        const displayName = normalizeRegionName(region.displayName);
        const displayNameBase = displayName.replace(/[시도]$/, '');

        return (
            displayName === normalizedLevel1Base ||
            displayNameBase === normalizedLevel1 ||
            displayNameBase === normalizedLevel1Base
        );
    })?.[0];

    return fuzzyMatchedCode as RegionCode | undefined;
}

export function findRegionByReverseGeo(response: ReverseGeoLike): MatchedRegion | undefined {
    const regionCode = findRegionCodeByLevel1(response.principalSubdivision);
    if (!regionCode) {
        return undefined;
    }

    const region = REGIONS[regionCode];
    const candidates = [response.city, response.locality]
        .map((value) => value?.trim())
        .filter((value): value is string => Boolean(value))
        .map((value) => normalizeRegionName(value));

    const matchedSub = region.sub.find((sub) => candidates.includes(normalizeRegionName(sub.displayName)));

    return {
        code: regionCode,
        displayName: region.displayName,
        subDisplayName: matchedSub?.displayName,
        label: matchedSub ? `${region.displayName} ${matchedSub.displayName}` : region.displayName
    };
}
